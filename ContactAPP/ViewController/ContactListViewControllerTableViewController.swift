import UIKit

class ContactListViewControllerTableViewController: UITableViewController {

    var contactList = PersonMVP.getRandomContact()
    
    override func viewDidLoad() {
        super.viewDidLoad()
    }

    // MARK: - Table view data source
    
    override func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        
        return contactList.count 
    }

  
    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "cell", for: indexPath)

        var ferstContact = contactList[indexPath.row]
        cell.textLabel?.text = ferstContact.name + " " +  ferstContact.secondName

        return cell
    }
    

   

    

